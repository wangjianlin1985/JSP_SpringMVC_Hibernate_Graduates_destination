package com.hyf.cemap.bean.bi;

public class BICountEmployRate {
    
    // 总人数
    private Integer all;
    
    // 就业总人数
    private Integer hasemploy;

    // 未就业总人数
    private Integer noemploy;

    public Integer getAll() {
        return all;
    }

    public void setAll(Integer all) {
        this.all = all;
    }

    public Integer getHasemploy() {
        return hasemploy;
    }

    public void setHasemploy(Integer hasemploy) {
        this.hasemploy = hasemploy;
    }

    public Integer getNoemploy() {
        return noemploy;
    }

    public void setNoemploy(Integer noemploy) {
        this.noemploy = noemploy;
    }
    
}
